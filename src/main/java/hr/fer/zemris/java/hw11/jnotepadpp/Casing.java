package hr.fer.zemris.java.hw11.jnotepadpp;

/**
 * Enumeration that represents types of case switching the Notepad++ application
 * must support.
 * 
 * @author Dinz
 *
 */
public enum Casing {
	/**
	 * Represents transforming letters to upper case.
	 */
	UPPERCASE,

	/**
	 * Represents transforming letters to lower case.
	 */
	LOWERCASE,

	/**
	 * Represents switching case of the letters.
	 */
	SWITCHCASE;
}
